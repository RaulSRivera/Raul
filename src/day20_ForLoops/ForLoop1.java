package day20_ForLoops;

public class ForLoop1 {
	public static void main(String[]args) {
		for (int l = 0; l < 10; l++) {
			System.out.println("Love Java!");
		}
		for (int p = 5; p <= 15; p++) {
			System.out.println(p);
		}
		for (int k = 20; k >= 10; k--) {
			System.out.print(k+" ");
		}
	}

}
